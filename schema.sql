CREATE TABLE picture (
id INTEGER PRIMARY KEY AUTOINCREMENT,
sha1 TEXT(40) NOT NULL,
dateTaken INTEGER NOT NULL ,
dateAdded INTEGER NOT NULL DEFAULT CURRENT_TIMESTAMP,
ts INTEGER DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE sets (
id INTEGER PRIMARY KEY AUTOINCREMENT,
name TEXT NOT NULL,
ts INTEGER DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE gallery (
id INTEGER PRIMARY KEY AUTOINCREMENT,
name TEXT NOT NULL,
title TEXT,
description TEXT,
ts INTEGER DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE tag (
id INTEGER PRIMARY KEY AUTOINCREMENT,
tag TEXT,
ts INTEGER DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE comments (
id INTEGER PRIMARY KEY AUTOINCREMENT,
comment TEXT,
id_picture INTEGER REFERENCES picture (id),
ts INTEGER DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE set_gallery (
id INTEGER PRIMARY KEY AUTOINCREMENT,
id_gallery INTEGER REFERENCES gallery (id),
id_set INTEGER REFERENCES sets (id),
ts INTEGER DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE tag_picture (
id INTEGER PRIMARY KEY AUTOINCREMENT,
id_tag INTEGER REFERENCES tag (id),
id_picture INTEGER REFERENCES picture (id),
ts INTEGER DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE set_picture (
id INTEGER PRIMARY KEY AUTOINCREMENT,
id_picture INTEGER REFERENCES picture (id),
id_set INTEGER REFERENCES sets (id),
ts INTEGER DEFAULT CURRENT_TIMESTAMP
);


